#### operaçoes com números
# +
# -
# *
# /
# % -> resto
# ** -> exponencial

# +
valor1 = 10
valor2 = 15

soma = valor1 + valor2
print(soma)

# -

valor1 = 20
valor2 = 10

diferenca = valor1 - valor2
print(diferenca)

# *

valor1 = 20
valor2 = 10

multiplicaçao = valor1 * valor2
print(multiplicaçao)

# /

valor1 = 30
valor2 = 20

divisao = valor1 / valor2
print(divisao)

#% resto

valor1 = 55
valor2 = 20

resto = valor1 % valor2
print(resto)

# ** exponencial

valor1 = 3
valor2 = 4

exponencial = valor1 ** valor2
print(exponencial)
